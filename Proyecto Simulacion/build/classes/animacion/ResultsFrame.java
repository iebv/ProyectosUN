    private void insertarDatos() {
        
        DecimalFormat df = new DecimalFormat("0");
        tfClientesAtendidos.setText(df.format(resultadosSimulacion.get(0)));
        tfMaximoClientes.setText(df.format(resultadosSimulacion.get(1)));
        tfDurCaja.setText(String.valueOf(round(resultadosSimulacion.get(2), 2)) + " min");
        tfDurServ.setText(String.valueOf(round(resultadosSimulacion.get(3), 2)) + " min");
        tfTotServ.setText(String.valueOf(round(resultadosSimulacion.get(4), 2)) + " min");
        tfEspMax.setText(String.valueOf(round(resultadosSimulacion.get(5), 2)) + " min");
        tfUsoCaja.setText(String.valueOf(round(resultadosSimulacion.get(6), 2)) + "%");
    }
    
    public double round(float numero, int decimales) {
        return Math.round(numero*Math.pow(10,decimales))/Math.pow(10,decimales);
    }
