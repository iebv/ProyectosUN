    private boolean checkLogin() throws SQLException {
        try {
            //Se crea conexion por default para verificar en la tabla usuario
            conexionMysql = new ConnectionMySql("default", "123", 1);
            user = "\"" + user + "\"";
            ResultSet rs = conexionMysql.sentencia.executeQuery("select rol, password_user, id_usuario from usuario where user_name = " + user);
            if (rs.next()) {
                if (rs.getString(1).equals("estudiante")) {
                    if (password.equals(rs.getString(2))) {
                        codigo = rs.getString(3);
                        return true;
                    } else {
                        JOptionPane.showMessageDialog(null, "Contraseña incorrecta");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "El usuario no tiene permisos para acceder");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Usuario inexistente");
            }
            return false;
        } catch (SQLException ex) {
            Logger.getLogger(ConsultaHistoricos.class.getName()).log(Level.SEVERE, null, ex);
            return false;
        } finally {
            conexionMysql.con.close();
        }
    }

