    private void bCertificadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCertificadoActionPerformed
        try {
            user = tfUsuario.getText();
            password = pfContraseña.getText();
            if (checkLogin()) {
                conexionMysql = new ConnectionMySql("estudiante", "789", 1);
                if (conexionMysql.connectionEstablished) { //Si se establece la conexión
                    //Se consultan los datos de la tabla certificado
                    codigo = "\"" + codigo + "\"";
                    String year = "\""  + String.valueOf(Calendar.getInstance().get(Calendar.YEAR)) + "\"" ; //Se obtiene el año actual
                    ResultSet rs = conexionMysql.sentencia.executeQuery("select nombre_estudiante, grado, cedula_acudiente, fecha, monto_matricula, documento_estudiante"
                            + " from certificado where codigo_estudiante = " + codigo + " and año_certificado = " + year);
                    String nombreEstudiante, grado, cedulaAcudiente, fechaMatricula, montoMatricula, documentoEstudiante; 
                    //Se asignan los datos a las variables
                    if(rs.next()){
                        nombreEstudiante = rs.getString(1);
                        grado = rs.getString(2);
                        cedulaAcudiente = rs.getString(3);
                        fechaMatricula = rs.getString(4);
                        montoMatricula = rs.getString(5);
                        documentoEstudiante = rs.getString(6);
                        //Crea nueva ventana de certificado
                        Certificado cert = new Certificado(nombreEstudiante, grado, cedulaAcudiente, fechaMatricula, montoMatricula, documentoEstudiante);
                        cert.setLocationRelativeTo(this);
                        cert.setVisible(true);
                    }else{
                        JOptionPane.showMessageDialog(null, "El certificado no existe");
                    }
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(ConsultaCertificado.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bCertificadoActionPerformed

